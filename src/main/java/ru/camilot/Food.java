package ru.camilot;

public class Food {

    public final String name;
    public final double p, f, c, k, cost;

    public Food(String name, double p, double f, double c, double k, double cost) {
        this.name = name;
        this.p = p;
        this.f = f;
        this.c = c;
        this.k = k;
        this.cost = cost;
    }
}
